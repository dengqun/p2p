package com.fzm.cgb;

public class Response {
private int code;
private String data;
private String log;
public int getCode() {
	return code;
}
public void setCode(int code) {
	this.code = code;
}
public String getData() {
	return data;
}
public void setData(String data) {
	this.data = data;
}
public String getLog() {
	return log;
}
public void setLog(String log) {
	this.log = log;
}

}
